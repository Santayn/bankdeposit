package org.santayn.bankdeposit.models;

/**
 * Роль пользователя в системе депозитного отдела банка.
 */
public enum UserRole {

    /**
     * Администратор системы: управляет пользователями, имеет полный доступ.
     */
    ADMIN,

    /**
     * Директор: доступ к отчётам и основным операциям.
     */
    DIRECTOR,

    /**
     * Менеджер: работа с клиентами и договорами.
     */
    MANAGER,

    /**
     * Операционист: проводит операции по депозитам.
     */
    OPERATOR,

    /**
     * Аудитор: доступ на чтение, просмотр отчётов и операций.
     */
    AUDITOR
}
